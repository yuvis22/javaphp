CREATE TABLE roombook (
    id INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    Email VARCHAR(255),
    Country VARCHAR(100),
    Phone VARCHAR(20),
    RoomType VARCHAR(50),
    Bed VARCHAR(50),
    NoofRoom INT,
    Meal VARCHAR(50),
    cin DATE,
    cout DATE,
    stat VARCHAR(50),
    nodays INT
);
